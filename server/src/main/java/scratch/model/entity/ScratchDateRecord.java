package scratch.model.entity;

import java.util.Date;

public class ScratchDateRecord {

	private Date date;

	private Integer times;

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public Integer getTimes() {
		return times;
	}

	public void setTimes(Integer times) {
		this.times = times;
	}
}
